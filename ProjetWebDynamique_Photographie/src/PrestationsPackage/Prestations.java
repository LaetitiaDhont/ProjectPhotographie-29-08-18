package PrestationsPackage;

public class Prestations {

	String type_prestation;
	String description_prestation;
	int prix;

	public String getType_prestation() {
		return type_prestation;
	}

	public void setType_prestation(String type_prestation) {
		this.type_prestation = type_prestation;
	}

	public String getDescription_prestation() {
		return description_prestation;
	}

	public void setDescription_prestation(String description_prestation) {
		this.description_prestation = description_prestation;
	}

	public int getPrix() {
		return prix;
	}

	public void setPrix(int prix) {
		this.prix = prix;
	}
}
